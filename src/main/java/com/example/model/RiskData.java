package com.example.model;

public class RiskData {
	
	
	int lowRisk;
	
	int mediumRisk;
	
	int highRisk;
	
	int veryHighRisk;

	int undefinedRisks;
	
	public int getLowRisk() {
		return lowRisk;
	}

	public void setLowRisk(int lowRisk) {
		this.lowRisk = lowRisk;
	}

	public int getMediumRisk() {
		return mediumRisk;
	}

	public void setMediumRisk(int mediumRisk) {
		this.mediumRisk = mediumRisk;
	}

	public int getHighRisk() {
		return highRisk;
	}

	public void setHighRisk(int highRisk) {
		this.highRisk = highRisk;
	}

	public int getVeryHighRisk() {
		return veryHighRisk;
	}

	public void setVeryHighRisk(int veryHighRisk) {
		this.veryHighRisk = veryHighRisk;
	}

	public int getUndefinedRisks() {
		return undefinedRisks;
	}

	public void setUndefinedRisks(int undefinedRisks) {
		this.undefinedRisks = undefinedRisks;
	}
	

	
	
}
