package com.example.model;

public class ProductConfiguration{
	
	private String projectId;
	
	private String customerValue;
	
	private String deliveryOptionValue;
	
	private String technologyOptionValue;
	
	private String productFeature;
	
	private String marketOptionValue;

	public String getProjectId() {
		return projectId;
	}

	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}

	public String getCustomerValue() {
		return customerValue;
	}

	public void setCustomerValue(String customerValue) {
		this.customerValue = customerValue;
	}

	public String getDeliveryOptionValue() {
		return deliveryOptionValue;
	}

	public void setDeliveryOptionValue(String deliveryOptionValue) {
		this.deliveryOptionValue = deliveryOptionValue;
	}

	public String getTechnologyOptionValue() {
		return technologyOptionValue;
	}

	public void setTechnologyOptionValue(String technologyOptionValue) {
		this.technologyOptionValue = technologyOptionValue;
	}

	public String getProductFeature() {
		return productFeature;
	}

	public void setProductFeature(String productFeature) {
		this.productFeature = productFeature;
	}

	public String getMarketOptionValue() {
		return marketOptionValue;
	}

	public void setMarketOptionValue(String marketOptionValue) {
		this.marketOptionValue = marketOptionValue;
	}
	
	
	
}